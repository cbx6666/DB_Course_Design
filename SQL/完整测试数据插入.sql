-- 验证用户数据
SELECT '用户数据' AS 数据类型, COUNT(*) AS 数量 FROM USERS WHERE USERID >= 3;

-- 验证商家数据
SELECT '商家数据' AS 数据类型, COUNT(*) AS 数量 FROM SELLERS WHERE USERID >= 3;

-- 验证店铺数据
SELECT '店铺数据' AS 数据类型, COUNT(*) AS 数量 FROM STORES WHERE STOREID >= 100;

-- 验证优惠券数据
SELECT '优惠券数据' AS 数据类型, COUNT(*) AS 数量 FROM COUPON_MANAGERS WHERE COUPONMANAGERID >= 2000;

-- 查看优惠券详情
SELECT 
    COUPONMANAGERID,
    COUPONNAME,
    CASE COUPONTYPE 
        WHEN 0 THEN '满减券' 
        WHEN 1 THEN '折扣券' 
        ELSE '未知类型' 
    END AS 优惠券类型,
    MINIMUMSPEND AS 最低消费,
    DISCOUNTAMOUNT AS 优惠金额,
    DISCOUNTRATE AS 折扣率,
    TOTALQUANTITY AS 总数量,
    USEDQUANTITY AS 已使用,
    VALIDFROM AS 开始时间,
    VALIDTO AS 结束时间,
    STOREID AS 店铺ID,
    SELLERID AS 商家ID
FROM COUPON_MANAGERS 
WHERE COUPONMANAGERID >= 2000 
ORDER BY COUPONMANAGERID;

-- 统计信息
SELECT 
    COUNT(*) AS 总优惠券数量,
    SUM(CASE WHEN COUPONTYPE = 0 THEN 1 ELSE 0 END) AS 满减券数量,
    SUM(CASE WHEN COUPONTYPE = 1 THEN 1 ELSE 0 END) AS 折扣券数量,
    SUM(TOTALQUANTITY) AS 总发放数量,
    SUM(USEDQUANTITY) AS 总使用数量
FROM COUPON_MANAGERS 
WHERE COUPONMANAGERID >= 2000;

